package edu.leonardo.tiposVariaveis;
public class constantes {   
    public static void main(String[] args) {
        //Constantes em java:


        //Uma constante é uma variavel que não pode ter seu valor alterado durante a execução do programa
        //Para declarar uma constante em java utilizamos a palavra reservada final antes do tipo da variavel
        //Por convenção, o nome das constantes é sempre em letras maiusculas
        //Exemplo:
        final double PI = 3.14;
        final int ANO_NASCIMENTO = 2010;


        //Exibindo os valores na tela
        System.out.println(PI);
        System.out.println(ANO_NASCIMENTO);
    }
    
}
